// 1. Unary Opertor
package javapackage;

public class Operators1 {

	public static void main(String[] args) {
		int a=5;
		System.out.println(a);//5
		System.out.println(a++);//5
		System.out.println(a);//6
		System.out.println(a++);//6
		System.out.println(a);//7
		System.out.println(++a);//8
		System.out.println(a);//8
		System.out.println(a--);//8
		System.out.println(a);//7
		System.out.println(--a);//6
		
		
	}

}
